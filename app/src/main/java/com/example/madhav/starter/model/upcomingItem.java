package com.example.madhav.starter.model;

public class upcomingItem {
    private String head;
    private String desc;

    public upcomingItem(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
